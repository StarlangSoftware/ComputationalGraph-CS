    public void CreateIrisDataSet(List<Tensor> trainSet, List<Tensor> testSet)
    {
        int[] strides = [5];
        for (var i = 0; i < 150; i++)
        {
            var inputData = new List<double>();
            for (var j = 0; j < 5; j++)
            {
                inputData.Add(IrisData[i, j]);
            }

            var input = new Tensor(inputData, strides);
            if (i % 5 != 0)
            {
                trainSet.Add(input);
            }
            else
            {
                testSet.Add(input);
            }
        }
    }

    protected Tensor CreateInputTensor(Tensor instance)
    {
        var data = new List<double>();
        for (var i = 0; i < instance.GetShape()[0] - 1; i++)
        {
            data.Add(instance.GetValue([i]));
        }

        return new Tensor(data, [1, instance.GetShape()[0] - 1]);
    }

    public override void Train(List<Tensor> trainSet, NeuralNetworkParameter parameters)
    {
    }

    public override ClassificationPerformance Test(List<Tensor> testSet)
    {
        int count = 0, total = 0;
        foreach (var instance in testSet)
        {
            var input = InputNodes[0];
            input.SetValue(CreateInputTensor(instance));
            var output = Predict();
            var classLabel = output[0];
            int[] index = [instance.GetShape()[0] - 1];
            if (classLabel == (int)instance.GetValue(index))
            {
                count++;
            }

            total++;
        }

        return new ClassificationPerformance(count / (total + 0.0));
    }

    protected override List<int> GetClassLabels(ComputationalNode outputNode)
    {
        var classIndices = new List<int>();
        var outputValue = outputNode.GetValue();
        var cols = outputValue.GetShape()[1];
        double maxValue = -1;
        var labelIndex = -1;
        int[] indices = [0, 0];
        for (var i = 0; i < cols; i++) {
            indices[1] = i;
            var value = outputValue.GetValue(indices);
            if (value > maxValue) {
                maxValue = value;
                labelIndex = i;
            }
        }
        classIndices.Add(labelIndex);
        return classIndices;
    }
